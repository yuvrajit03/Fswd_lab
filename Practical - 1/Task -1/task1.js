// var
var var_x = 10;
document.write("Original value of var_x (var):", var_x + "<br>");
var_x = 20;
document.write(" New value of var_x (var):", var_x + "<br>");
// let
let let_x = "Hello";
document.write("Value of let_x (let):", let_x + "<br>");
let_x = "World";
document.write("New value of let_x (let):", let_x + "<br>");

// const
const const_x = [1, 2, 3];
document.write("Value of const_x (const):", const_x + "<br>");
document.write("New value of const_x (const):", const_x + "<br>");